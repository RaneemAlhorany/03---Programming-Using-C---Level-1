#include <iostream>
using namespace std;
int main()
{
	short number1, number2;

	cout << "Please  enter the first number A?" << endl;
	cin >> number1;
	cout << "Please  enter the second number B?" << endl;
	cin >> number2;

	cout << number1 << " + " << number2 << " = " << number1 + number2<<endl;
	cout << number1 << " - " << number2 << " = " << number1 - number2 << endl;
	cout << number1 << " * " << number2 << " = " << number1 * number2 << endl;
	cout << number1 << " / " << number2 << " = " << number1 / number2 << endl;
	cout << number1 << " % " << number2 << " = " << number1 % number2 << endl;



}


